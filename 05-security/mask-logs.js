export function maskPII(s) {
  if (typeof s !== "string") return s;
  return s
    .replace(
      /\b[\w.-]+@[\w.-]+\.\w+\b/g,
      (m) => m[0] + "***" + m.slice(m.indexOf("@"))
    )
    .replace(
      /\b(\+?\d{1,3})?[\s-]?\d{3}[\s-]?\d{3}[\s-]?\d{2}[\s-]?\d{2}\b/g,
      "***-***-**-**"
    );
}
